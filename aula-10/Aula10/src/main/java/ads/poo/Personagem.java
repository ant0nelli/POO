package ads.poo;

public class Personagem {
    private int forca;
    private int altura;
    private int agilidade;
    private String arma;

    public void setForca(int a){
        forca = a;
    }

    public int getForca(){
        return forca;
    }

    public void setAltura(int b){
        altura = b;
    }

    public int getAltura(){
        return altura;
    }

    public void setAgilidade(int c){
        agilidade = c;
    }

    public int getAgilidade(){
        return agilidade;
    }

    public void setArma(String d){
        arma = d;
    }

    public String getArma(){
        return arma;
    }

}
